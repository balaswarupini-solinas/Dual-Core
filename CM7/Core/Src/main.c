/**
* @brief Function implementing the SharedMemRead thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_SharedMemoryReadTaskFunc */
void SharedMemoryReadTaskFunc(void *argument)
{
  /* USER CODE BEGIN SharedMemoryReadTaskFunc */
  /* Infinite loop */
	SensorData_t sensor;

	    for(;;)
	    {
	        while(HAL_HSEM_FastTake(0)!=HAL_OK);

	        sensor.temperature =
	                shared_data->temperature;

	        sensor.temperature =
	            (uint16_t)(sensor.temperature * 100);

	        sensor.humidity =
	                shared_data->humidity;

	        sensor.humidity =
	            (uint16_t)(sensor.humidity * 100);

	        sensor.pressure =
	                shared_data->pressure;

	        sensor.pressure =
	            (uint16_t)(sensor.pressure / 100);

	        HAL_HSEM_Release(0,0);

	        osMessageQueuePut(
	                sensorQueueHandle,
	                &sensor,
	                0,
	                0);

	        osDelay(1000);
	    }
  /* USER CODE END SharedMemoryReadTaskFunc */
}

/* USER CODE BEGIN Header_UartTaskFunc */
/**
* @brief Function implementing the SharedMemoryTas thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_UartTaskFunc */
void UartTaskFunc(void *argument)
{
  /* USER CODE BEGIN UartTaskFunc */
  /* Infinite loop */

	    SensorData_t sensor;

	    for(;;)
	    {
	        HAL_UART_Receive(
	                &huart3,
	                rx_pkt,
	                8,
	                10);

	        if(rx_pkt[0] == 0xA5 &&
	           rx_pkt[1] == 0x01 &&
	           rx_pkt[7] == 0x5A)
	        {
	        	app_data[0] = rx_pkt[0];
	        	app_data[1] = rx_pkt[1];
	        	app_data[7] = rx_pkt[7];
	            osMessageQueueGet(
	                    sensorQueueHandle,
	                    &sensor,
	                    NULL,
	                    osWaitForever);

	            send_packet(1, sensor.temperature);
	            send_packet(2, sensor.humidity);
	            send_packet(3, sensor.pressure);
	            memset(rx_pkt, 0 , sizeof(rx_pkt));
	                    }
	    }
  /* USER CODE END UartTaskFunc */
}

